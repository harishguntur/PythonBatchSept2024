#!/usr/bin/pyhton3
import math
#A= p(1+r/n) pownt 

principal_balance = int(input("enter principal balance:"))
intrest_rate = float(input("enter annual interest rate:"))
time = int(input("enter time:"))
n = int(input("No of times interest is compounded per year:"))

#calculating final amount
final_amount = principal_balance*(1+(intrest_rate/n))

#calculating power
sel  = n*time
compound_intrest=pow(sel,final_amount)

print("Compound Intrest:",compound_intrest)